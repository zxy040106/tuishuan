def verify_liuyao(result: dict) -> list:
    """
    六爻装卦自检。
    返回问题列表，空列表表示无问题。
    """
    issues = []

    zhuanggua = result.get("装卦", {})
    if "error" in zhuanggua:
        issues.append(f"装卦失败: {zhuanggua['error']}")
        return issues

    # 检查爻辞是否6条
    yaos = zhuanggua.get("爻辞", [])
    if len(yaos) != 6:
        issues.append(f"爻辞数量异常: 期望6条, 实际{len(yaos)}条")

    # 检查世应
    has_shi = any(y.get("世应") == "世" for y in yaos)
    has_ying = any(y.get("世应") == "应" for y in yaos)
    if not has_shi:
        issues.append("缺少世爻标记")
    if not has_ying:
        issues.append("缺应爻标记")

    # 检查六亲完整性
    for y in yaos:
        if not y.get("六亲"):
            issues.append(f"{y['爻位']}缺少六亲")
        if not y.get("纳支"):
            issues.append(f"{y['爻位']}缺少纳支")
        if not y.get("六兽"):
            issues.append(f"{y['爻位']}缺少六兽")

    # 检查本卦是否存在
    if result.get("本卦") is None:
        issues.append("本卦未识别（上下卦组合不在六十四卦中）")

    return issues



def _find_fushen(ben_gua: dict, ben_liuqin: list, ben_nazhi: list, ben_nazhi_full: list) -> list:
    """
    查找伏神：检查本卦六亲中缺失的类型，到本宫卦中寻找。
    返回 [{六亲, 伏神, 飞神同位, 伏飞关系}] 列表。
    """
    all_liuqin_types = ["父母", "官鬼", "妻财", "子孙", "兄弟"]
    present = set(ben_liuqin)
    missing = [lq for lq in all_liuqin_types if lq not in present]
    if not missing:
        return []

    palace = ben_gua["palace"]
    ben_gong_gua = _HEXAGRAM_LOOKUP.get((palace, palace))
    if ben_gong_gua is None:
        return []

    bg_zhi = get_nazhi(ben_gong_gua, with_gan=False)
    bg_liuqin = [get_liuqin(palace, z) for z in bg_zhi]
    bg_nazhi_full = get_nazhi(ben_gong_gua, with_gan=True)

    result = []
    for lq_type in missing:
        for j, (blq, zhi, nazhi) in enumerate(zip(bg_liuqin, bg_zhi, bg_nazhi_full)):
            if blq == lq_type:
                yao_num = j + 1
                fei_nazhi = ben_nazhi_full[j] if j < len(ben_nazhi_full) else ""
                fei_lq = ben_liuqin[j] if j < len(ben_liuqin) else ""
                # 伏飞关系
                fivx = DIZHI_WUXING.get(zhi, "")
                fevx = DIZHI_WUXING.get(ben_nazhi[j], "") if j < len(ben_nazhi) else ""
                if fevx and fivx:
                    if wuxing_sheng(fevx) == fivx: rel = "飞来生伏（得长生，吉）"
                    elif wuxing_ke(fevx) == fivx: rel = "飞来克伏（被压制，凶）"
                    elif wuxing_sheng(fivx) == fevx: rel = "伏来生飞（泄气）"
                    elif wuxing_ke(fivx) == fevx: rel = "伏来克飞（出暴，透出则有力）"
                    elif fivx == fevx: rel = "伏飞比和（平和）"
                    else: rel = "无直接生克"
                else: rel = ""
                result.append({
                    "六亲": lq_type,
                    "伏神": f"第{yao_num}爻 {nazhi}({lq_type})",
                    "本宫卦": ben_gong_gua["full_name"],
                    "飞神": f"第{yao_num}爻 {fei_nazhi}({fei_lq})" if fei_nazhi else "",
                    "伏飞关系": rel,
                })
                break
    return result



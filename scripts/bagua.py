def meihua_from_datetime(dt: datetime = None) -> dict:
    """
    梅花易数 以当前时间起卦。
    年数+月数+日数 = 上卦
    年数+月数+日数+时数 = 下卦... 实际是:
    年月日之和 = 上卦
    年月日时之和 = 下卦...

    标准梅花易数时间卦:
    上卦: (年支数 + 月 + 日) ÷ 8 取余
    下卦: (年支数 + 月 + 日 + 时支数) ÷ 8 取余
    动爻: (年支数 + 月 + 日 + 时支数) ÷ 6 取余
    """
    if dt is None:
        dt = datetime.now()

    from ganzhi import get_year_ganzhi
    # 年支数: 以立春为界，精确到分钟
    year_gz = get_year_ganzhi(dt)
    year_zhi = year_gz[1]  # 取年支
    year_zhi_num = DIZHI.index(year_zhi) + 1  # 子=1, 丑=2, ..., 亥=12

    month_num = determine_calendar_lunar_month(dt)  # 农历月
    day_num = determine_nongli_day(dt)              # 农历日

    shichen = hour_to_shichen(dt.hour)
    shichen_num = shichen_index(shichen) + 1  # 子=1, 丑=2, ...

    upper_num = year_zhi_num + month_num + day_num
    lower_num = year_zhi_num + month_num + day_num + shichen_num
    change_num = lower_num  # 动爻以总和取

    upper = number_to_bagua(upper_num)
    lower = number_to_bagua(lower_num)
    dong_yao = number_to_yao(change_num)

    return meihua_pai_gua(upper, lower, dong_yao, "时间起卦")


def meihua_pai_gua(upper: str, lower: str, dong_yao: int, source: str = "三数起卦") -> dict:
    """梅花易数 排盘核心逻辑."""
    # 本卦
    ben_gua = get_hexagram_by_trigrams(upper, lower)

    # 互卦: 本卦二三四爻为下卦, 三四五爻为上卦
    # 需要从本卦获取每爻的阴阳
    # 简化处理: 用本卦的上下卦属性推导
    # 本卦下卦的2/3爻 + 上卦的1爻 = 互卦下卦
    # 本卦下卦的3爻 + 上卦的1/2爻 = 互卦上卦

    # 六十四卦中每爻的阴阳由卦的结构决定
    # 此处使用简化的八卦爻象
    trigram_yao = {
        "乾": "阳阳阳", "兑": "阳阳阴", "离": "阳阴阳", "震": "阳阴阴",
        "巽": "阴阳阳", "坎": "阴阳阴", "艮": "阴阴阳", "坤": "阴阴阴",
    }

    ben_lower_yao = trigram_yao[lower]   # 初、二、三
    ben_upper_yao = trigram_yao[upper]   # 四、五、上

    # 本卦六爻（初到上）
    all_yao = ben_lower_yao + ben_upper_yao  # 长度6, 初(0)到上(5)

    # 互卦下卦: 本卦二爻(1)、三爻(2)、四爻(3)
    hu_lower_pattern = all_yao[1] + all_yao[2] + all_yao[3]
    # 互卦上卦: 本卦三爻(2)、四爻(3)、五爻(4)
    hu_upper_pattern = all_yao[2] + all_yao[3] + all_yao[4]

    trigram_reverse = {v: k for k, v in trigram_yao.items()}
    hu_lower = trigram_reverse.get(hu_lower_pattern, lower)
    hu_upper = trigram_reverse.get(hu_upper_pattern, upper)

    hu_gua = get_hexagram_by_trigrams(hu_upper, hu_lower)

    # 变卦: 本卦动爻位置的爻变（阳变阴、阴变阳）
    dong_idx = dong_yao - 1  # 0-based
    yao_list = list(all_yao)
    yao_list[dong_idx] = "阴" if yao_list[dong_idx] == "阳" else "阳"

    bian_lower_pattern = "".join(yao_list[:3])
    bian_upper_pattern = "".join(yao_list[3:])
    bian_lower = trigram_reverse.get(bian_lower_pattern, lower)
    bian_upper = trigram_reverse.get(bian_upper_pattern, upper)

    bian_gua = get_hexagram_by_trigrams(bian_upper, bian_lower)

    # 体用: 无动爻的卦为体，有动爻的卦为用
    # 动爻在上卦 → 上卦为用，下卦为体
    # 动爻在下卦 → 下卦为用，上卦为体
    if dong_yao <= 3:
        ti_gua = upper
        yong_gua = lower
    else:
        ti_gua = lower
        yong_gua = upper

    # 体用五行生克
    ti_wx = BAGUA_ATTRS[ti_gua]["wu_xing"]
    yong_wx = BAGUA_ATTRS[yong_gua]["wu_xing"]

    # 体用生克
    rel = wuxing_relation(ti_wx, yong_wx)

    ti_yong_desc = {
        "同": "体用比和 — 大吉，事情顺利，主客和谐",
        "a生b": "体生用 — 耗费精力，付出多收获少，宜主动进取",
        "a克b": "体克用 — 可以掌控局面，但需付出努力",
        "a被b生": "用生体 — 吉兆，外界环境有利，事半功倍",
        "a被b克": "用克体 — 凶兆，外部压力大，事情受阻",
    }

    # 互卦体用：体卦位置跟随本卦动爻所在上下卦
    # 动爻在下卦 → 本卦下卦为用, 上卦为体。互卦中同样：下卦=用, 上卦=体
    # 动爻在上卦 → 本卦上卦为用, 下卦为体。互卦中同样：上卦=用, 下卦=体
    if hu_gua:
        if dong_yao <= 3:
            # 动爻在下卦 → 体=上卦, 用=下卦
            hu_ti_gua = hu_upper
            hu_yong_gua = hu_lower
        else:
            # 动爻在上卦 → 体=下卦, 用=上卦
            hu_ti_gua = hu_lower
            hu_yong_gua = hu_upper
        hu_ti_wx = BAGUA_ATTRS[hu_ti_gua]["wu_xing"]
        hu_yong_wx = BAGUA_ATTRS[hu_yong_gua]["wu_xing"]
        hu_rel = wuxing_relation(hu_ti_wx, hu_yong_wx)
        hu_ti_yong = ti_yong_desc.get(hu_rel, "")
    else:
        hu_ti_gua = hu_ti_wx = hu_yong_wx = ""
        hu_rel = ""
        hu_ti_yong = ""

    # 变卦体用：动爻阴阳反转后，体卦的上下卦位置不变
    # 但体卦本身可能因为动爻变化而变成另一个卦
    # 例如：本卦上乾(体)下震(用), 动爻在下卦3爻 → 下卦震变离
    # 变卦中 体卦仍是上卦(乾不变), 用卦变成了离
    if bian_gua:
        if dong_yao <= 3:
            # 动爻在下卦 → 变卦的下卦(用)变了, 上卦(体)不变
            bian_ti_gua = upper  # 体卦=上卦, 没有动爻所以和本卦相同
            bian_yong_gua = bian_lower  # 用卦变了
        else:
            # 动爻在上卦 → 变卦的上卦(用)变了, 下卦(体)不变
            bian_ti_gua = lower  # 体卦=下卦, 没有动爻所以和本卦相同
            bian_yong_gua = bian_upper  # 用卦变了
        bian_ti_wx = BAGUA_ATTRS[bian_ti_gua]["wu_xing"]
        bian_yong_wx = BAGUA_ATTRS[bian_yong_gua]["wu_xing"]
        bian_rel = wuxing_relation(bian_ti_wx, bian_yong_wx)
        bian_ti_yong = ti_yong_desc.get(bian_rel, "")
        # 体用生克变化追踪（本卦→变卦）
        if rel == bian_rel:
            bian_change = "体用关系不变，事态走向平稳"
        elif ti_yong_desc.get(bian_rel, "").startswith(("体用比和","用生体")):
            if ti_yong_desc.get(rel, "").startswith(("用克体","体生用")):
                bian_change = "体用由凶转吉，最终能走出困境"
            else:
                bian_change = "体用趋吉，结局向好"
        elif ti_yong_desc.get(bian_rel, "").startswith(("用克体","体生用")):
            if ti_yong_desc.get(rel, "").startswith(("体用比和","用生体")):
                bian_change = "体用由吉转凶，开始顺利但结局需谨慎"
            else:
                bian_change = "体用趋凶，最终阶段可能有阻力"
        else:
            bian_change = "体用关系有变化，需具体分析"
    else:
        bian_ti_gua = bian_ti_wx = bian_yong_wx = ""
        bian_rel = ""
        bian_ti_yong = ""
        bian_change = ""

    # 梅花易数应期: 先天卦数之和
    bagua_num_map = {"乾":1,"兑":2,"离":3,"震":4,"巽":5,"坎":6,"艮":7,"坤":8}
    ti_num = bagua_num_map.get(ti_gua, 1)
    yong_num = bagua_num_map.get(yong_gua, 1)
    yingqi_shu = ti_num + yong_num + dong_yao
    yingqi_hints = f"卦数应期参考：本卦数{ti_num}+用卦数{yong_num}+动爻{dong_yao}={yingqi_shu}（可取{yingqi_shu}日/周/月为应期参考）"

    # 卦辞爻辞（本卦+变卦+动爻）
    ben_gua_name = ben_gua["name"] if ben_gua else ""
    ben_guaci = get_gua_yaoci(ben_gua_name)
    ben_yaoci_full = (ben_guaci.get("爻辞", {}).get(dong_yao, "") if ben_guaci else "")
    ben_xci = ben_guaci.get("彖辞", "") if ben_guaci else ""
    ben_xiang = ben_guaci.get("象辞", "") if ben_guaci else ""

    bian_gua_name = bian_gua["name"] if bian_gua else ""
    bian_guaci = get_gua_yaoci(bian_gua_name) if bian_gua_name else None

    return {
        "本卦": ben_gua,
        "互卦": hu_gua,
        "变卦": bian_gua,
        "动爻": dong_yao,
        "体卦": ti_gua,
        "用卦": yong_gua,
        "体卦五行": ti_wx,
        "用卦五行": yong_wx,
        "体用关系": rel,
        "体用解读": ti_yong_desc.get(rel, ""),
        "互卦体卦": hu_ti_gua,
        "互卦用卦": hu_yong_gua,
        "互卦体用关系": hu_rel,
        "互卦体用解读": hu_ti_yong,
        "变卦体卦": bian_ti_gua,
        "变卦用卦": bian_yong_gua,
        "变卦体用关系": bian_rel,
        "变卦体用解读": bian_ti_yong,
        "体用变化": bian_change,
        "本卦卦辞": ben_guaci.get("卦辞", "") if ben_guaci else "",
        "本卦彖辞": ben_xci,
        "本卦象辞": ben_xiang,
        "动爻爻辞": ben_yaoci_full,
        "变卦卦辞": bian_guaci.get("卦辞", "") if bian_guaci else "",
        "应期参考": yingqi_hints,
        "起卦方式": source,
    }



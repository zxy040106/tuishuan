# 六十四卦每爻纳支（由上下卦纳支组合）
# 下卦管初二三爻, 上卦管四五上爻


def get_nazhi(hexagram: dict, with_gan: bool = True) -> list:
    """
    返回六爻纳支序列 [初爻, 二爻, 三爻, 四爻, 五爻, 上爻].
    当 with_gan=True 时返回纳甲完整写法（如 '甲子'），否则仅返回地支。
    """
    lower = hexagram["lower"]
    upper = hexagram["upper"]

    # 下卦纳支（取前三爻）
    if lower in NAZHI_YANG:
        lower_nazhi = NAZHI_YANG[lower][:3]
    else:
        lower_nazhi = NAZHI_YIN[lower][:3]

    # 上卦纳支（取后三爻）
    if upper in NAZHI_YANG:
        upper_nazhi = NAZHI_YANG[upper][3:]
    else:
        upper_nazhi = NAZHI_YIN[upper][3:]

    zhi_list = lower_nazhi + upper_nazhi

    if not with_gan:
        return zhi_list

    # 纳干: 京房八宫纳甲 — 内卦(初二三爻)用内卦纳干, 外卦(四五上爻)用外卦纳干
    lower_gan = NAGAN_INNER.get(lower, "")
    upper_gan = NAGAN_OUTER.get(upper, "")
    result = []
    for i, z in enumerate(zhi_list):
        g = lower_gan if i < 3 else upper_gan
        result.append(g + z)
    return result



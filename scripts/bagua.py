    # 加上日辰和月建
    candidates = set(dong_zhi)
    if day_zhi:
        candidates.add(day_zhi)
    if month_zhi:
        candidates.add(month_zhi)

    sanhe_results = []
    zhi_list = list(candidates)
    for i in range(len(zhi_list)):
        for j in range(i + 1, len(zhi_list)):
            for k in range(j + 1, len(zhi_list)):
                wx = get_dizhi_sanhe(zhi_list[i], zhi_list[j], zhi_list[k])

def liuyao_construct(lines: list) -> dict:
    """
    从六次摇卦结果构建卦象。

    lines: 6个整数 [初爻值, 二爻值, ..., 上爻值]
    每爻值: 6=老阴(⚋→⚊), 7=少阳(⚊), 8=少阴(⚋), 9=老阳(⚊→⚋)

    返回: {
        本卦, 变卦, 动爻列表,
        本卦卦象(阴阳爻), 变卦卦象
    }
    """
    if len(lines) != 6:
        raise ValueError(f"六爻起卦需要6个数字，实际收到{len(lines)}个。请补齐6次摇卦结果。")
    for v in lines:
        if v not in (6, 7, 8, 9):
            raise ValueError(f"摇卦值必须为6/7/8/9之一，收到: {v}")
    # 本卦阴阳爻: 6=阴, 7=阳, 8=阴, 9=阳
    ben_lines = ["阳" if v in (7, 9) else "阴" for v in lines]

    # 变卦阴阳爻: 6→阳, 7→阳(不变), 8→阴(不变), 9→阴
    bian_lines = []
    for v in lines:
        if v == 6:
            bian_lines.append("阳")
        elif v == 9:
            bian_lines.append("阴")
        else:
            bian_lines.append("阳" if v == 7 else "阴")

    # 动爻索引 (1-based, 6和9都是动爻)
    dongyao = [i + 1 for i, v in enumerate(lines) if v in (6, 9)]

    # 本卦和变卦的上下卦
    def lines_to_trigrams(ll):
        """将6个阴阳爻转为上下卦名（先天八卦序）."""
        # 下卦: 初二三爻
        lower_bin = "".join(["1" if x == "阳" else "0" for x in ll[:3]])
        # 上卦: 四五上爻
        upper_bin = "".join(["1" if x == "阳" else "0" for x in ll[3:]])

        # 二进制转先天八卦数: 1=乾(111), 2=兑(110), 3=离(101), 4=震(100),
        #                   5=巽(011), 6=坎(010), 7=艮(001), 8=坤(000)
        # 注意: 爻从下往上, 二进制从左到右表示下到上
        xiantian_map = {
            "111": "乾", "110": "兑", "101": "离", "100": "震",
            "011": "巽", "010": "坎", "001": "艮", "000": "坤",
        }
        return xiantian_map[lower_bin], xiantian_map[upper_bin]

    ben_lower, ben_upper = lines_to_trigrams(ben_lines)
    bian_lower, bian_upper = lines_to_trigrams(bian_lines)

    # 找卦
    ben_gua = get_hexagram_by_trigrams(ben_upper, ben_lower)
    bian_gua = get_hexagram_by_trigrams(bian_upper, bian_lower)

    return {
        "摇卦记录": lines,
        "本卦": ben_gua,
        "变卦": bian_gua,
        "动爻": dongyao,
        "本卦阴阳爻": ben_lines,
        "变卦阴阳爻": bian_lines,
        "本卦上下卦": (ben_upper, ben_lower),
        "变卦上下卦": (bian_upper, bian_lower),
    }


def liuyao_zhuanggua(hexagram_result: dict, day_ganzhi: str,
                      month_zhi: str = None) -> dict:
    """
    六爻装卦：纳支、六亲、六兽、世应。

    hexagram_result: liuyao_construct() 的输出
    day_ganzhi: 占卦当日的干支，如 '甲子'
    month_zhi: 月建地支（可选但推荐），用于精确判断暗动/日破

    返回: 完整的装卦结果
    """
    ben_gua = hexagram_result["本卦"]
    bian_gua = hexagram_result["变卦"]
    dongyao = hexagram_result["动爻"]
    day_gan, day_zhi = split_ganzhi(day_ganzhi)

    if ben_gua is None:
        return {"error": "本卦未找到"}

    palace = ben_gua["palace"]
    shi_pos = ben_gua["shi"]

    # 世应
    shiying = get_shiying(shi_pos)

    # 本卦纳支（含纳干）
    ben_nazhi_full = get_nazhi(ben_gua, with_gan=True)
    ben_nazhi = get_nazhi(ben_gua, with_gan=False)
    # 变卦纳支（含纳干）
    if bian_gua:
        bian_nazhi_full = get_nazhi(bian_gua, with_gan=True)
        bian_nazhi = get_nazhi(bian_gua, with_gan=False)
    else:
        bian_nazhi_full = [""] * 6
        bian_nazhi = [""] * 6

    # 本卦六亲
    ben_liuqin = [get_liuqin(palace, zhi) for zhi in ben_nazhi]
    # 变卦六亲
    bian_liuqin = [get_liuqin(palace, zhi) for zhi in bian_nazhi] if bian_gua else [""] * 6

    # 六兽
    liushou = get_liushou(day_gan)

    # 组装每爻信息
